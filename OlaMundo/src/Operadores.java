public class Operadores {
    public static void main(String[] args){
        int a = 145;
        int b = 45;

        System.out.println("O número é " + a);
        System.out.println("O outro número é " + b);

        a += 15;
        b -= 40;

        System.out.println("A soma do número é igual a " + a );
        System.out.println("A subtração do outro número é igual a " + b);

        a /= 15;
        b *= 8;

        System.out.println("A divisão do número é " + a);
        System.out.println("A multiplicação do outro número é " + b);
    }
}
