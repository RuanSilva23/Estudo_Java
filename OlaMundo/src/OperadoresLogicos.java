public class OperadoresLogicos {
    public static void main(String[] args){
        boolean a = true;
        boolean b = false;

        //Operador lógico AND (&&):
        System.out.println("a && b: " + (a && b));

        //Operador lógico OR (||):
        System.out.println("a || b: " + (a || b));

        //Operador lógico NOT (!):
        System.out.println("!a: " + (!a));
        System.out.println("!b: " + (!b));
    }
}
